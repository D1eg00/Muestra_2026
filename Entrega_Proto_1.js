// ── Clase Curva ───────────────────────────────
class Curva {
  constructor(col, pts) {
    this.c      = col;
    this.puntos = pts;
    this.x      = 0;
    this.y      = 0;
  }

  dibujar() {
    fill(this.c);
    if (this === seleccionada) {
      stroke(255, 255, 255, 180);
      strokeWeight(1.5);
    } else {
      noStroke();
    }
    beginShape();
    for (let i = 0; i < this.puntos.length; i += 2) {
      curveVertex(this.puntos[i] + this.x, this.puntos[i + 1] + this.y);
    }
    endShape(CLOSE);
    noStroke();
  }

  estaSobre(mx, my) {
    let minX =  99999, maxX = -99999;
    let minY =  99999, maxY = -99999;
    for (let i = 0; i < this.puntos.length; i += 2) {
      let px = this.puntos[i]     + this.x;
      let py = this.puntos[i + 1] + this.y;
      if (px < minX) minX = px;
      if (px > maxX) maxX = px;
      if (py < minY) minY = py;
      if (py > maxY) maxY = py;
    }
    return mx >= minX && mx <= maxX && my >= minY && my <= maxY;
  }
}
